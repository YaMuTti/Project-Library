package com.group.bookloan.admin;

public class Admin {
	private int admin_id;
	private String ad_id;
	private String ad_pass;
	private String ad_name;
	private String ad_regdate;
	
	public int getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(int admin_id) {
		this.admin_id = admin_id;
	}
	public String getAd_id() {
		return ad_id;
	}
	public void setAd_id(String ad_id) {
		this.ad_id = ad_id;
	}
	public String getAd_pass() {
		return ad_pass;
	}
	public void setAd_pass(String ad_pass) {
		this.ad_pass = ad_pass;
	}
	public String getAd_name() {
		return ad_name;
	}
	public void setAd_name(String ad_name) {
		this.ad_name = ad_name;
	}
	public String getAd_regdate() {
		return ad_regdate;
	}
	public void setAd_regdate(String ad_regdate) {
		this.ad_regdate = ad_regdate;
	}
}
